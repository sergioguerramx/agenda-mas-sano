create or replace view public.patient_branch_activity
with (security_invoker = true)
as
select
  h.patient_id,
  h.branch_id,
  min(coalesce(h.original_scheduled_at, h.scheduled_at)) as first_appointment_at,
  max(coalesce(h.original_scheduled_at, h.scheduled_at)) as last_appointment_at,
  max(coalesce(h.original_scheduled_at, h.scheduled_at)) filter (where h.attended is true) as last_attended_at,
  count(*)::integer as total_appointments,
  count(*) filter (where h.attended is true)::integer as attended_appointments,
  count(*) filter (
    where h.confirmed is true
      and h.attended is not true
      and coalesce(h.original_scheduled_at, h.scheduled_at) < now()
  )::integer as confirmed_without_attendance,
  count(*) filter (where h.released_at_8)::integer as released_appointments,
  bool_or(
    coalesce(h.original_scheduled_at, h.scheduled_at) >= now()
    and not h.released_at_8
    and not h.cancelled
    and h.attended is not true
  ) as has_future_appointment
from public.patient_appointment_history h
group by h.patient_id, h.branch_id;

create or replace view public.patient_activity_summary
with (security_invoker = true)
as
with activity as (
  select
    p.id as patient_id,
    p.full_name,
    p.whatsapp,
    p.whatsapp_status,
    p.promotional_consent,
    min(coalesce(h.original_scheduled_at, h.scheduled_at)) as first_appointment_at,
    max(coalesce(h.original_scheduled_at, h.scheduled_at)) as last_appointment_at,
    max(coalesce(h.original_scheduled_at, h.scheduled_at)) filter (where h.attended is true) as last_attended_at,
    count(h.id)::integer as total_appointments,
    count(h.id) filter (where h.attended is true)::integer as attended_appointments,
    count(h.id) filter (
      where h.confirmed is true
        and h.attended is not true
        and coalesce(h.original_scheduled_at, h.scheduled_at) < now()
    )::integer as confirmed_without_attendance,
    count(h.id) filter (where h.released_at_8)::integer as released_appointments,
    bool_or(
      coalesce(h.original_scheduled_at, h.scheduled_at) >= now()
      and not h.released_at_8
      and not h.cancelled
      and h.attended is not true
    ) as has_future_appointment
  from public.patient_profiles p
  left join public.patient_appointment_history h on h.patient_id = p.id
  group by p.id, p.full_name, p.whatsapp, p.whatsapp_status, p.promotional_consent
), last_branch as (
  select distinct on (h.patient_id)
    h.patient_id,
    h.branch_id as last_branch_id
  from public.patient_appointment_history h
  order by h.patient_id, coalesce(h.original_scheduled_at, h.scheduled_at) desc, h.created_at desc
)
select
  a.*,
  lb.last_branch_id,
  case
    when coalesce(a.has_future_appointment, false) then 'con_cita_futura'
    when a.attended_appointments = 0 and a.confirmed_without_attendance > 0 then 'agendo_no_acudio'
    when a.attended_appointments = 0 then 'sin_asistencia_comprobada'
    when a.attended_appointments = 1 then 'primera_consulta_sin_regreso'
    when a.last_attended_at >= now() - interval '30 days' then 'activa'
    when a.last_attended_at >= now() - interval '60 days' then 'seguimiento'
    when a.last_attended_at >= now() - interval '120 days' then 'inactiva'
    else 'reactivacion'
  end as segment_key,
  (
    a.whatsapp_status = 1
    and a.promotional_consent = 1
    and not coalesce(a.has_future_appointment, false)
  ) as promotion_ready
from activity a
left join last_branch lb on lb.patient_id = a.patient_id;

create or replace function public.sync_operational_appointment_to_history(p_appointment_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  a public.appointments%rowtype;
  patient_uuid uuid;
  branch_smallint smallint;
  service_smallint smallint;
  combined_name text;
  combined_name_key text;
  appointment_timestamp timestamptz;
begin
  select * into a from public.appointments where id = p_appointment_id;
  if not found then return; end if;

  combined_name := regexp_replace(btrim(concat_ws(' ', a.first_name, a.last_name)), '\s+', ' ', 'g');
  combined_name_key := lower(combined_name);

  select id into branch_smallint from public.branches where code = 'SN';

  select p.id into patient_uuid
  from public.patient_profiles p
  where p.whatsapp = a.whatsapp
    and p.name_key = combined_name_key
  order by p.created_at
  limit 1;

  if patient_uuid is null then
    insert into public.patient_profiles (
      full_name,
      whatsapp,
      whatsapp_status,
      promotional_consent
    ) values (
      combined_name,
      case when a.whatsapp ~ '^\+[1-9][0-9]{7,14}$' then a.whatsapp else null end,
      case when a.whatsapp ~ '^\+[1-9][0-9]{7,14}$' then 1 else 0 end,
      1
    ) returning id into patient_uuid;
  end if;

  select id into service_smallint
  from public.services_catalog
  where code = coalesce(a.service, 'sesion_integral_399')
  limit 1;

  appointment_timestamp := (a.appointment_date + a.appointment_time) at time zone 'America/Monterrey';

  insert into public.patient_appointment_history (
    patient_id,
    branch_id,
    service_id,
    scheduled_at,
    confirmed,
    attended,
    released_at_8,
    cancelled,
    source_kind,
    source_event_key,
    calendar_event_id,
    legacy_appointment_id,
    confidence,
    updated_at
  ) values (
    patient_uuid,
    branch_smallint,
    service_smallint,
    appointment_timestamp,
    case when a.status in ('confirmed', 'completed') then true else null end,
    case when a.status = 'completed' then true else null end,
    a.appointment_time = time '08:00',
    a.status = 'cancelled',
    case when a.origin = 'yosoysano' then 4 else 1 end,
    'current:' || a.id::text,
    a.google_calendar_event_id,
    a.id,
    100,
    now()
  )
  on conflict (legacy_appointment_id) do update set
    patient_id = excluded.patient_id,
    branch_id = excluded.branch_id,
    service_id = excluded.service_id,
    scheduled_at = excluded.scheduled_at,
    confirmed = excluded.confirmed,
    attended = excluded.attended,
    released_at_8 = excluded.released_at_8,
    cancelled = excluded.cancelled,
    calendar_event_id = excluded.calendar_event_id,
    updated_at = now();
end;
$$;

create or replace function public.mirror_operational_appointment_to_history()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  begin
    perform public.sync_operational_appointment_to_history(new.id);
  exception when others then
    raise warning 'No se pudo reflejar la cita % en el historial: %', new.id, sqlerrm;
  end;
  return new;
end;
$$;

drop trigger if exists mirror_operational_appointment_history on public.appointments;
create trigger mirror_operational_appointment_history
after insert or update of first_name, last_name, whatsapp, appointment_date, appointment_time, status, google_calendar_event_id, service
on public.appointments
for each row execute function public.mirror_operational_appointment_to_history();

-- Integra las citas que ya existen sin alterar appointments ni contacts.
do $$
declare existing_appointment record;
begin
  for existing_appointment in select id from public.appointments loop
    perform public.sync_operational_appointment_to_history(existing_appointment.id);
  end loop;
end;
$$;

