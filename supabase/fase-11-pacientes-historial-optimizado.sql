create table if not exists public.patient_profiles (
  id uuid primary key default gen_random_uuid(),
  full_name text not null,
  name_key text generated always as (
    lower(regexp_replace(btrim(full_name), '\s+', ' ', 'g'))
  ) stored,
  whatsapp text,
  whatsapp_status smallint not null default 0 check (whatsapp_status in (0, 1)),
  promotional_consent smallint not null default 1 check (promotional_consent in (-1, 0, 1)),
  source_patient_key text unique,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (
    (whatsapp_status = 0 and whatsapp is null)
    or
    (whatsapp_status = 1 and whatsapp ~ '^\+[1-9][0-9]{7,14}$')
  )
);

