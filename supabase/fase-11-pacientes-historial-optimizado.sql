create table if not exists public.patient_appointment_history (
  id uuid primary key default gen_random_uuid(),
  patient_id uuid not null references public.patient_profiles(id) on delete cascade,
  branch_id smallint not null references public.branches(id),
  nutritionist_id smallint references public.nutritionists(id) on delete set null,
  service_id smallint references public.services_catalog(id) on delete set null,
  scheduled_at timestamptz not null,
  original_scheduled_at timestamptz,
  sequence_number smallint check (sequence_number is null or sequence_number > 0),
  confirmed boolean,
  attended boolean,
  released_at_8 boolean not null default false,
  cancelled boolean not null default false,
  source_kind smallint not null check (source_kind in (1, 2, 3, 4)),
  source_event_key text unique,
  calendar_event_id text,
  legacy_appointment_id uuid unique references public.appointments(id) on delete set null,
  confidence smallint not null default 100 check (confidence between 0 and 100),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (not (attended is true and (released_at_8 or cancelled)))
);

